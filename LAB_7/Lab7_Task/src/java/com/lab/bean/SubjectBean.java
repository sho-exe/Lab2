package com.lab.bean;

public class SubjectBean implements java.io.Serializable {

    private String id;
    private String matric_no;
    private String subject_name;
    private String subject_code;

    // No-arg constructor
    public SubjectBean() {
    }

    // Getter and Setter for id
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    // Getter and Setter for matric_no
    public String getMatric_no() {
        return matric_no;
    }

    public void setMatric_no(String matric_no) {
        this.matric_no = matric_no;
    }

    // Getter and Setter for subject_name
    public String getSubject_name() {
        return subject_name;
    }

    public void setSubject_name(String subject_name) {
        this.subject_name = subject_name;
    }

    // Getter and Setter for subject_code
    public String getSubject_code() {
        return subject_code;
    }

    public void setSubject_code(String subject_code) {
        this.subject_code = subject_code;
    }
}